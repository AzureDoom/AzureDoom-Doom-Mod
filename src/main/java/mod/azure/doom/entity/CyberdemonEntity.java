	private <E extends Entity> boolean animationPredicate(AnimationTestEvent<E> event) {
		if (!(limbSwingAmount > -0.15F && limbSwingAmount < 0.15F)) {
			controller.setAnimation(new AnimationBuilder().addAnimation("walking", true));
			return true;
		} 
		return false;
	}

